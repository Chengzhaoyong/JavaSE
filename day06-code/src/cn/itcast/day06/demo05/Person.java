package cn.itcast.day06.demo05;

public class Person {
		private int age;
		private String name;
		private boolean male;//是不是爷们
		
		
		public boolean isMale() {
			return male;
		}
		public void setMale(boolean male) {
			this.male = male;
		}
		public int getAge() {
			return age;
		}
		public void setAge(int age) {
			this.age = age;
		}
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		
		
}
